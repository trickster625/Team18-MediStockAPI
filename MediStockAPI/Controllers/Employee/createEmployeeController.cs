using MediStockAPI.Models;
using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;
using System.Net;
using System.Net.Http;
using System.Security.Cryptography;
using System.Text;
using System.Web.Http;

namespace MediStockAPI.Controllers
{
    public class createEmployeeController : ApiController
    {
        MediStock_DBEntities db = new MediStock_DBEntities();
        [HttpPost]
        [Route("createEmployee")]
        public IHttpActionResult createEmployee(Models.Employee newEmployee)
        {
            try
            {
                newEmployee.Employee_HashedPassword = Encrypt(newEmployee.Employee_HashedPassword);
                db.Employees.Add(newEmployee);
                db.SaveChangesAsync();

                return Ok("Employee Added!");
            }
            catch (Exception)
            {

                return BadRequest();
            }
        }

        public string Encrypt(string clearText)
        {
            string EncryptionKey = "MAKV2SPBNI99212";
            byte[] clearBytes = Encoding.Unicode.GetBytes(clearText);
            using (Aes encryptor = Aes.Create())
            {
                Rfc2898DeriveBytes pdb = new Rfc2898DeriveBytes(EncryptionKey, new byte[] { 0x49, 0x76, 0x61, 0x6e, 0x20, 0x4d, 0x65, 0x64, 0x76, 0x65, 0x64, 0x65, 0x76 });
                encryptor.Key = pdb.GetBytes(32);
                encryptor.IV = pdb.GetBytes(16);
                using (MemoryStream ms = new MemoryStream())
                {
                    using (CryptoStream cs = new CryptoStream(ms, encryptor.CreateEncryptor(), CryptoStreamMode.Write))
                    {
                        cs.Write(clearBytes, 0, clearBytes.Length);
                        cs.Close();
                    }
                    clearText = Convert.ToBase64String(ms.ToArray());
                }
            }
            return clearText;
        }
    }
}
